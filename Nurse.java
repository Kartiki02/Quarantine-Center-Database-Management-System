    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DBCursor cursor = null;
        try {
        MongoClient mongoClient= new MongoClient( "localhost" , 27017 );
        DB db = mongoClient.getDB( "my_db" );
        DBCollection coll = db.getCollection("nurse");
        cursor = coll.find();

        String[] columnNames = { "Name", "Qualification"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        while(cursor.hasNext()) {
        DBObject obj = cursor.next();
        
        String name = (String)obj.get("Name");
        String qualification = (String)obj.get("Qualification");
        
        model.addRow(new Object[] { name,qualification});
        }
        jTable1.setModel(model);
        cursor.close(); 
        mongoClient.close();
        }       
        catch (Exception e) {
            Logger.getLogger(Patients.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

